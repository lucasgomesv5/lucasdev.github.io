let currentLang = "pt";

function updateLanguage(lang) {
  currentLang = lang;
  document.querySelectorAll("[data-i18n]").forEach(el => {
    const key = el.getAttribute("data-i18n");
    const value = translations[lang][key];

    if (Array.isArray(value)) {
      el.innerHTML = value.map(item => `<li>${item}</li>`).join("");
    } else {
      el.textContent = value || key;
    }
  });

  const toggleBtn = document.getElementById("toggle-lang");
  toggleBtn.textContent = lang === "pt" ? "🇬🇧 English" : "🇧🇷 Português";
}

document.getElementById("toggle-lang").addEventListener("click", () => {
  updateLanguage(currentLang === "pt" ? "en" : "pt");
});

updateLanguage(currentLang);

// Controle de abas
const tabLinks = document.querySelectorAll(".tab-link");
const tabContents = document.querySelectorAll(".tab-content");

tabLinks.forEach(link => {
  link.addEventListener("click", (e) => {
    e.preventDefault();
    const target = link.getAttribute("data-tab");

    tabLinks.forEach(l => l.classList.remove("active"));
    link.classList.add("active");

    tabContents.forEach(section => {
      section.classList.remove("active");
      if (section.getAttribute("data-content") === target) {
        section.classList.add("active");
      }
    });
  });
});
