let currentLang = "pt";

function updateLanguage(lang) {
  currentLang = lang;
  document.querySelectorAll("[data-i18n]").forEach(el => {
    const key = el.getAttribute("data-i18n");
    const value = translations[lang][key];

    if (Array.isArray(value)) {
      el.innerHTML = value.map(item => `<li>${item}</li>`).join("");
    } else {
      el.textContent = value || key;
    }
  });

  const toggleBtn = document.getElementById("toggle-lang");
  toggleBtn.textContent = lang === "pt" ? "ðŸ‡¬ðŸ‡§ English" : "ðŸ‡§ðŸ‡· PortuguÃªs";
}

document.getElementById("toggle-lang").addEventListener("click", () => {
  updateLanguage(currentLang === "pt" ? "en" : "pt");
});

